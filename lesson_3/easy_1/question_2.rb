what is != and where should you use it?     # => not equals
put ! before something, like !user_name     # => turns any object into the opposite boolean.
put ! after something, like words.uniq!     # => mean that it's a destructive method & part of the method
put ? before something                      # => ternary operator
put ? after something                       # => part of the method
put !! before something, like !!user_name   # => It will turn the object to the same boolean.